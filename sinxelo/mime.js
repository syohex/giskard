var mime = {

	type: function(extension) {

		var result = false;

		if (extension) {

			result = extensions[extension.toLowerCase()];

			if (result) {
				result = result.join("/");
			}
		}


		return result;
	},

	extensions: function(type) {

		if (!type) {
			return false;
		}

		return types[type.toLowerCase()];
	}

};

